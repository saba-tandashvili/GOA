def compare_numbers(num1, num2):
    print(f"{num1} > {num2}: {num1 > num2}")
    print(f"{num1} >= {num2}: {num1 >= num2}")
    print(f"{num1} < {num2}: {num1 < num2}")
    print(f"{num1} <= {num2}: {num1 <= num2}")
    print(f"{num1} == {num2}: {num1 == num2}")
    print(f"{num1} != {num2}: {num1 != num2}")

compare_numbers(5, 10)
